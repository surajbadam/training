public class emp {
    private String name;
    private int age;
    private int salary;

    public void setName(String name){
        this.name=name;
    }
    public String getName(){
        return this.name;
    }
    public void setAge(int age)
    {
        this.age=age;
    }
    public int getAge(){
        return age;
    }
    public void setSal(int salary)
    {
        this.salary=salary;
    }
    public int getSal(){
        return salary;
    }
}
